package demo;

public class Myname20 {

	public static void main(String[] args) {

		System.out.println(" For Loop");

		for (int count = 20; count > 0; count--) {

			System.out.println("Manasa");
		}

		System.out.println("Do while Loop");

		int i = 0;

		do {

			System.out.println("Manasa");
			i++;

		}

		while (i < 20);

		System.out.println("While Loop");

		int x = 0;

		while (x < 20)
			
		{

			System.out.println("Manasa");
			x++;

		}

	}
}
